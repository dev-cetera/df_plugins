import 'package:flutter/widgets.dart';

import '../dart_src/plugin.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

@immutable
abstract class WidgetPlugin<W extends Widget> extends Plugin {
  const WidgetPlugin();
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

abstract class WidgetPluginManager<W extends Widget, P extends WidgetPlugin<W>>
    extends PluginManager<P> {
  WidgetPluginManager({super.plugins});

  @override
  W Function({required BuildContext context, required W child}) get build;
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

abstract class WidgetPluginBuilder<W extends Widget, P extends WidgetPlugin<W>>
    extends StatelessWidget {
  final List<P> plugins;
  final W child;
  const WidgetPluginBuilder({
    super.key,
    required this.plugins,
    required this.child,
  });
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

@immutable
abstract class AttachableWidgetPlugin<W extends Widget> extends WidgetPlugin<W> {
  const AttachableWidgetPlugin();

  W attach(BuildContext context, Widget child);
}
